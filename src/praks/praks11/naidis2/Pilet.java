package praks.praks11.naidis2;

public class Pilet {
    private int koht;
    private String tehinguId;

    public Pilet(int koht, String tehinguId) {
        this.koht = koht;
        this.tehinguId = tehinguId;
    }

    public int getKoht() {
        return koht;
    }

    public String getTehinguId() {
        return tehinguId;

    }
}
